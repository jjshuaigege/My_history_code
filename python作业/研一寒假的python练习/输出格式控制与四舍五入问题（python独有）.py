#%s %d %f %.nf分别代表字符串，整数，浮点数，输出小数并且小数点后保留n位
'''
两种方法
"{1:.2f} and {0:d}".format(101,3.14)
"%.2f and %d"%(3.14,101)
'''
print("%.0f"%22.5)     #刚好为5且后面数字全为0，则前一位为偶数则舍去，为奇数则进位，其它与正常情况一致
print("%.0f"%23.5)
print(round(22.5))
print(round(23.5))
print("{1:s} and {0:d}".format(101,"sgz"))
print("%d and %s"%(101,"sgz"))